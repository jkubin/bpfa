static int find_id(const void *ptr1, const void *ptr2)
{

	unsigned int id1 = (*(struct mcu_info **)ptr1)->proc_id;
	unsigned int id2 = (*(struct mcu_info **)ptr2)->proc_id;

	if (id1 < id2)
		return -1;

	if (id1 > id2)
		return 1;

	return 0;
}

static int find_name(const void *ptr1, const void *ptr2)
{
	return strcmp((*(struct mcu_info *)ptr1).name, (*(struct mcu_info *)ptr2).name);
}

unsigned int bpfa_part_id(char *name)
{

	struct mcu_info *ptr;
	struct mcu_info key;

	key.name = name;

	ptr = bsearch(&key, &mcu_id, ARRAY_SIZE(mcu_id),
			sizeof(struct mcu_info), find_name);

	if (!ptr) {

		printf("unknown part name %s\n", name);

		return 0;
	}

	return ptr->proc_id;
}

char *bpfa_part_name(unsigned int proc_type)
{

	struct mcu_info **res;
	struct mcu_info *ptr;
	struct mcu_info key;

	key.proc_id = proc_type;

	ptr = &key;

	res = bsearch(&ptr, &proc_id, ARRAY_SIZE(proc_id),
			sizeof(struct mcu_info *), find_id);

	if (!res)
		return "unknown part id";

	return (*res)->name;
}

